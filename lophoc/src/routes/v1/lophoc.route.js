const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const lophocValidation = require('../../validations/lophoc.validation');
const lophocController = require('../../controllers/lophoc.controller');

const router = express.Router();

router
  .route('/')
  .post(validate(lophocValidation.createLophoc), lophocController.createLophoc)
  .get(validate(lophocValidation.getLophocs), lophocController.getLophocs);

router
  .route('/:id')
  .get(auth('getLophocs'), validate(lophocValidation.getLophoc), lophocController.getLophoc)
  .patch(validate(lophocValidation.updateLophoc), lophocController.updateLophoc)
  .delete(validate(lophocValidation.deleteLophoc), lophocController.deleteLophoc);

module.exports = router;

